% UQ_DOC   display the UQLab documentation.
%    UQ_DOC retrieve and display a list of the available UQLab User Manuals
%
%    In the UQLab software, each module is referenced by a CODE and has its
%    own user manual. This command allows you to list the codes of manuals
%    currently available, and to open them in either HTML or PDF formats.
%    It also provides their citation to reference them in publications.
%
%    UQ_DOC without input arguments lists all the available manuals and
%    their corresponding codes 
%
%    UQ_DOC(CODE) opens the manual of the module CODE in your browser (HTML
%    format) 
%
%    UQ_DOC(CODE,'HTML') opens the manual of the module CODE in a web
%    browser (HTML format) 
%
%    UQ_DOC(CODE,'PDF') opens the manual of the module CODE in PDF format.
%
%    UQ_DOC(CODE,'Cite') displays the reference to the manual of the CODE
%    module, to be used in scientific publications 
%    
